
public class Node {

	private int value;

	private Node nextNode;

	public Node() {
		this.value = value;
		nextNode = null;
	}

	public Node(int value) {
		this.value = value;
		nextNode = null;
	}

	public void setNextNode(Node nextNode) {
		this.nextNode = nextNode;
	}

	public Node getNextNode() {
		return nextNode;
	}

	public int getValue() {
		return value;
	}
	
	
}
